# Вспомогательная функция
def get_camera_matrix(width, height):
    """
    Получить матрицу камеры, масштабированную под заданное разрешение.

    Базовые значения рассчитаны для 640x480. При другом разрешении
    фокусные расстояния масштабируются пропорционально.

    Args:
        width:  ширина кадра (пиксели)
        height: высота кадра (пиксели)

    Returns:
        camera_matrix: numpy array 3x3
    """
    matrix = CAMERA_MATRIX_640x480.copy()
    matrix[0, 0] *= width / 640.0    # fx
    matrix[1, 1] *= height / 480.0   # fy
    matrix[0, 2] = width / 2.0       # cx
    matrix[1, 2] = height / 2.0      # cy
    return matrix
